/**
 * @brief  检查1ms软件定时器是否超时（非阻塞式）
 * @note   基于 DWT 周期计数器实现，无需占用硬件定时器中断。
 *         采用问询机制，适合在主循环中轮询判断时间片。
 * @retval true  1ms 周期已到达
 * @retval false 未到达
 * @warning 当前版本采用 "重置为当前时间" 的方式，在主循环受阻时会产生时间漂移(累积误差)。
 *          建议优化为步进式重置：gs_startTimeBase[0] += 1000UL * CPU_FREQ_MHZ;
 *          另外，以 == 0 作为首次初始化判断，存在 CYCCNT 溢出归零时误触发的极低概率风险。
 */
bool TIMER_HAL_Is1msTickTimeout(void)
{
    /* 第一次调用自动初始化起始时间 */
    if (gs_startTimeBase[0] == 0)
    {
        gs_startTimeBase[0] = DWT->CYCCNT;
    }

    if(DWT_TIMEOUT_CHECK(gs_startTimeBase[0], 1000))
    {
        gs_startTimeBase[0] = DWT->CYCCNT;
        return true;
    }
    return false;
}
/**
 * @brief  检查100ms软件定时器是否超时（非阻塞式）
 * @note   机制同 Is1msTickTimeout，仅超时阈值不同。
 * @retval true  100ms 周期已到达
 * @retval false 未到达
 * @warning 同样存在时间漂移和 == 0 初始化的隐患，建议同构优化。
 */
bool TIMER_HAL_Is100msTickTimeout(void)
{
    /* 第一次调用自动初始化起始时间 */
    if (gs_startTimeBase[1] == 0)
    {
        gs_startTimeBase[1] = DWT->CYCCNT;
    }

    if(DWT_TIMEOUT_CHECK(gs_startTimeBase[1], 100000))
    {
        gs_startTimeBase[1] = DWT->CYCCNT;
        return true;
    }
    return false;
}
/**
 * @brief  获取伪随机数种子
 * @note   嵌入式系统缺乏真实熵源，本函数采用多源异或策略增加随机性：
 *         1. DWT周期计数器：提供运行时微秒级的动态变化
 *         2. 局部变量地址(&seed)：利用栈空间地址的运行时随机性
 *         3. 行号(__LINE__)：提供编译时的静态位混淆
 *         适用于防总线冲突、防固定周期重试等场景，非密码学安全级别。
 * @retval 生成的32位伪随机种子
 * @note   在汽车 Bootloader 中，如果几十个 ECU 同时上电，同时请求升级（比如出厂下线场景），如果它们都固定等待 100ms 再重试，就会永远撞车，这叫“活锁”。
           你需要给每个 ECU 一个随机退避时间（比如 A 等待 105ms，B 等待 112ms）。
           此时调用 GetRandomSeed() 传给 srand()，再生成退避时间。因为几十个 ECU 虽然跑着相同的代码，但它们的上电时序有微秒级差异（电源纹波不同）、栈地址可能不同，生成的种子就不同，从而完美错开重试时间，解除总线拥堵。
 */
uint32_t TIMER_HAL_GetRandomSeed(void)
{
    uint32_t seed = DWT->CYCCNT;
    seed ^= (uint32_t)&seed;          // 栈地址随机性
    seed ^= (uint32_t)__LINE__;       // 编译时信息
    return seed;
}