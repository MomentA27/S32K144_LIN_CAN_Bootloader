/**
 * @brief   初始化看门狗模块
 * @note    调用底层驱动初始化函数，传入实例编号及预设配置结构体。
 *          初始化后看门狗即开始计数，需在超时前调用 WATCHDOG_HAL_Fed()。
 */
void WATCHDOG_HAL_Init(void)
{
    /* 调用底层驱动，使用看门狗实例 1 及默认配置 */
    WDOG_DRV_Init(INST_WATCHDOG1, &watchdog1_Config0);
}

/**
 * @brief   反初始化看门狗模块
 * @note    当前无任何操作。部分硬件在看门狗使能后不允许软件禁用，
 *          故此处仅保留空函数体以维持 API 完整性。
 */
void WATCHDOG_HAL_Deinit(void)
{
    /* 预留接口，暂无操作 */
}

/**
 * @brief   喂狗操作
 * @note    触发看门狗服务例程，重置内部计数器。
 *          上层应用应在主循环或定时中断中周期调用。
 */
void WATCHDOG_HAL_Fed(void)
{
    WDOG_DRV_Trigger(INST_WATCHDOG1);
}

/**
 * @brief   触发系统软件复位
 * @note    将超时值设为 0，使得下次看门狗刷新时立即复位 MCU。
 *          调用此函数后系统将立即重启，不会返回。
 */
void WATCHDOG_HAL_SystemRest(void)
{
    WDOG_DRV_SetTimeout(INST_WATCHDOG1, 0u);
}